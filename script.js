function getRandomFonts() {
  let random = Math.floor(Math.random() * fonts.length);
  return fonts[random].family;
}
getRandomFonts();

function loadFonts(fontfamily) {
  let link = document.createElement("link");
  link.rel = "stylesheet";
  link.href = `https://fonts.googleapis.com/css2?family=${fontfamily.replace(
    / /g,
    "+"
  )}:wght@300&display=swap`;
  document.head.appendChild(link);
}
function getThreeUniqueFonts() {
  let copy = [...fonts]; // make a copy
  copy.sort(() => Math.random() - 0.5); // shuffle array

  return [copy[0].family, copy[1].family, copy[2].family];
}

function applyFontsEverWhere() {
  let [font1, font2, font3] = getThreeUniqueFonts();

  loadFonts(font1);
  loadFonts(font2);
  loadFonts(font3);

  document.querySelector("#left-font1 h5").style.fontFamily = font1;
  document.querySelector("#left-font1 h5").textContent = font1;
  document.querySelector("#h1").style.fontFamily = font1;

  document.querySelector("#left-font2").style.fontFamily = font2;
  document.querySelector("#left-font2 h5").textContent = font2;
  document.querySelector("#h2").style.fontFamily = font2;

  document.querySelector("#left-font3").style.fontFamily = font3;
  document.querySelector("#left-font3 h5").textContent = font3;
  document.querySelector("#h3").style.fontFamily = font3;
}
document
  .querySelector(".generate button")
  .addEventListener("click", function () {
    applyFontsEverWhere();
  });
